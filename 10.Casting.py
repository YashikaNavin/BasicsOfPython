# Type Casting

# int()
a=int(1)
b=int(2.89)
c=int("45")
#d=int("35.78") this will show error as the given literal is not in integer form.
print("a = ", a)
print("b = ", b)
print("c = ", c)
#print("d = ", d)

# float()
e=float(25)
f=float(7485.8475)
g=float('5472')
print("e = ", e)
print("f = ", f)
print("g = ", g)

# str()
h=str(35)
i=str(487.49)
j=str("Hello")
print("h = ", h)
print("i = ", i)
print("j = ", j)
